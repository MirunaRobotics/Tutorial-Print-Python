# Print con variables

Hola = "Hello"    # Definimos "Hola" a "Helllo"

print(Hola)

# Imprimir varias cosas:

Python = "Miruna"

Youtube = "TikTok"

print("Estoy viendo", Python, "en", Youtube) # Usamos las comas para "unir" variables, strings o otras cosas


# Print de input

tutorial = input("Que estás viendo?") # Al definir una variable con un input, nos preguntará el texto para saber a que equivale
print("Estás viendo", tutorial) 


